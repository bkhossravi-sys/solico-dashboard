import streamlit as st

# تنظیمات اصلی برای حذف حاشیه های اضافی در موبایل
st.set_page_config(page_title="Market Matrix", layout="centered")

# تیتر ساده و استاندارد
st.title("📊 ماتریکس هوشمند بازار")
st.info("تحلیل زنده قیمت‌ها و لیدرهای بازار - اسفند ۱۴۰۴")

# دیتابیس اصلاح شده (بدون غلط املایی)
market_data = {
    "سس": [
        {"برند": "مهرام (۹۷۰ گرم)", "قیمت": 520000, "سهم": 35, "شهر": "تهران", "ویژگی": "🌟 محبوب‌ترین"},
        {"برند": "کاله (سولیکو)", "قیمت": 485000, "سهم": 13, "شهر": "آمل", "ویژگی": "🎨 زیباترین بسته‌بندی"},
        {"برند": "دلپذیر (۹۰۰ گرم)", "قیمت": 490000, "سهم": 28, "شهر": "مشهد", "ویژگی": "🛡️ قدرت توزیع"},
        {"برند": "بیژن (۹۰۰ گرم)", "قیمت": 510000, "سهم": 15, "شهر": "شیراز", "ویژگی": "💎 پریمیوم"}
    ]
}

# باکس جستجوی ساده
user_input = st.text_input("🔍 نام محصول را بنویسید (مثلاً سس):")

if user_input:
    # جستجوی کلمه در دیتابیس
    if "سس" in user_input:
        st.subheader("نتایج تحلیل برای دسته سس مایونز")
        
        # نمایش به صورت کارت های استاندارد Streamlit (بدون خطای کد خام)
        for item in market_data["سس"]:
            with st.container():
                # ایجاد یک قاب تمیز برای هر محصول
                st.write(f"### {item['ویژگی']} | {item['برند']}")
                
                col1, col2 = st.columns(2)
                with col1:
                    st.metric("قیمت مصرف‌کننده", f"{item['قیمت']:,} تومان")
                with col2:
                    st.write(f"📍 **منطقه نفوذ:** {item['شهر']}")
                    st.write(f"📈 **سهم بازار:** {item['سهم']}%")
                
                st.write("---") # خط جداکننده
        
        # بخش تحلیل نهایی جمینای
        st.success("💡 **تحلیل استراتژیک نهایی:**")
        st.write(f"""
        1. **محبوبیت:** برند **مهرام** با قیمت {market_data['سس'][0]['قیمت']:,} تومان، لیدر بلامنازع وفاداری مشتری است.
        2. **طراحی:** برند **کاله** بهترین نمره را در زیبایی بسته‌بندی و طراحی مدرن کسب کرده است.
        3. **ارزش خرید:** با توجه به شکاف قیمتی، **کاله** در حال حاضر اقتصادی‌ترین انتخاب برای مصرف‌کننده است.
        """)
        
    else:
        st.error("❌ محصول در دیتابیس یافت نشد. لطفاً 'سس' را جستجو کنید.")
else:
    st.warning("👆 لطفاً نام محصول را در کادر بالا وارد کنید تا تحلیل نمایش داده شود.")

import streamlit as st

# تنظیمات پایه برای خوانایی در موبایل
st.set_page_config(page_title="Market Intelligence", layout="centered")

# تیتر اصلی با استفاده از دستور استاندارد
st.title("📊 ماتریکس هوشمند بازار")
st.caption("بر اساس آخرین استعلام قیمت: اسفند ۱۴۰۴")

# دیتابیس بر اساس تصاویر ارسالی شما (ایمالز و ترب)
market_data = {
    "سس": [
        {"برند": "مهرام (۹۷۰ گرم)", "قیمت": 520000, "سهم": 35, "شهر": "تهران", "لقب": "🌟 محبوب‌ترین"},
        {"برند": "کاله (سولیکو)", "قیمت": 485000, "سهم": 13, "شهر": "آمل", "لقب": "🎨 زیباترین بسته‌بندی"},
        {"برند": "دلپذیر (۹۰۰ گرم)", "قیمت": 490000, "سهم": 28, "شهر": "مشهد", "لقب": "📉 رقیب توزیع"},
        {"برند": "بیژن (۹۰۰ گرم)", "قیمت": 510000, "سهم": 15, "شهر": "شیراز", "لقب": "💎 پریمیوم"}
    ]
}

# باکس جستجو
query = st.text_input("🔍 نام محصول را جستجو کنید (مثلاً سس):", "")

if query:
    if "سس" in query:
        st.subheader("نتایج تحلیل لیدرهای بازار")
        
        # نمایش هر محصول در یک باکس جداگانه و استاندارد
        for item in market_data["سس"]:
            with st.expander(f"{item['لقب']} | {item['برند']}", expanded=True):
                # استفاده از ستون‌های استاندارد برای جلوگیری از به‌هم‌ریختگی
                c1, c2 = st.columns(2)
                with c1:
                    st.metric("قیمت (تومان)", f"{item['قیمت']:,}")
                with c2:
                    st.write(f"📍 **منطقه نفوذ:** {item['شهر']}")
                    st.write(f"📈 **سهم بازار:** {item['sهم']}%")
        
        st.divider()
        
        # بخش تحلیل نهایی (بدون کد HTML برای جلوگیری از خطا)
        st.info("💡 **تحلیل استراتژیک جمینای:**")
        st.write("""
        - **لیدر محبوبیت:** برند مهرام با وجود قیمت ۵۲۰ هزار تومانی، همچنان لیدر سنتی بازار است.
        - **لیدر طراحی:** کاله (سولیکو) با بسته‌بندی‌های مدرن، بیشترین جذابیت بصری را در قفسه‌ها دارد.
        - **ارزش خرید:** برند کاله با قیمت ۴۸۵ هزار تومان، بهینه ترین قیمت را نسبت به لیدر بازار ارائه می‌دهد.
        """)
        
    else:
        st.warning("محصول مورد نظر یافت نشد. لطفاً 'سس' را جستجو کنید.")
else:
    st.write("👆 برای شروع، نام محصول را در کادر بالا وارد کنید.")
